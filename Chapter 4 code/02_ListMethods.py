# Methods of list 
list=[20,2233,1,2,55,11]
print(list)

#sort() return ascending order of list
list.sort()
print(list)

#append() add element at the en of list
list.append(6555)
print(list)

# insert(index,element) add element at any index which you mentioned
list.insert(0,44)
print(list)

#reverse() returns list in descending order
list.reverse()
print("Reverse list is:",list)

#pop(index) removes element from certain index
list.pop(3)
print(list)

#remove(ele) return list where we remove element directly
list.remove(6555)
print(list)